let w = document.getElementById("w");
w.addEventListener("click", () => {
    w.setAttribute("style", "opacity:0.3");
    let w1 = new Audio("sounds/w.mp3");
    w1.play();
    });
w.addEventListener("mouseleave",()=>{
    w.removeAttribute("style", "opacity:0.3");
});
